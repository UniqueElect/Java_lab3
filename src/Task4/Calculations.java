package Task4;

public class Calculations {

    public static void increment(final int first, final int second) {
        System.out.println(first+second + " increment completed");
    }

    public static void decrement(final int first, final int second) {
        System.out.println(first-second + " decrement completed");
    }

    public static void multi(final int first, final int second) {
        System.out.println(first*second + " multiplication completed");
    }

    public static void division(final double first, final double second) {
        System.out.println(first/second + " division completed");
    }
}